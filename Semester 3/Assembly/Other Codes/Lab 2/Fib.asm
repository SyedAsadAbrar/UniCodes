;; A PROGRAM TO ADD TWO NUMBERS 

;; USING REGISTERS 

[ORG 0x0100]	
	



MOV AX, 1
MOV BX, 1
MOV CX, 0
ADD CX, BX ; AX=1, BX=1, CX=1
ADD CX, AX ; AX=1, BX=1, CX=2
ADD AX, CX ; AX=2, BX=1, CX=2
ADD CX, AX ; AX=2, BX=1, CX=4
ADD BX, AX ; AX=2, BX=3, CX=4
ADD CX, BX ; AX=2, BX=3, CX=7
ADD AX, BX ; AX=5, BX=3, CX=7
ADD CX, AX ; AX=5, BX=3, CX=12
ADD BX, AX ; AX=5, BX=8, CX=12
ADD CX, BX ; AX=5, BX=8, CX=20
ADD AX, BX ; AX=13, BX=8, CX=20
ADD CX, AX ; AX=13, BX=8, CX=33
ADD BX, AX ; AX=13, BX=21, CX=33
ADD CX, BX ; AX=13, BX=21, CX=54
ADD AX, BX ; AX=34, BX=21, CX=54
ADD CX, AX; AX=34, BX=21, CX=88


 

MOV  AX, 0x4C00	
INT 0x21	
